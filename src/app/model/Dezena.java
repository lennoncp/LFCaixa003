package app.model;

public class Dezena {
	
	private Integer dezena;
	private Integer valor;
	
	public Dezena(Integer dezena, Integer valor) {
		super();
		this.dezena = dezena;
		this.valor = valor;
	}
	public Integer getDezena() {
		return dezena;
	}
	public void setDezena(Integer dezena) {
		this.dezena = dezena;
	}
	public Integer getValor() {
		return valor;
	}
	public void setValor(Integer valor) {
		this.valor = valor;
	}
	
	
	

}
